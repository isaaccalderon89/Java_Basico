package estructurasRepetitivas;

public class EstructuraFor {

	public static void main(String[] args) {
		/*
		 * La estructura para (For) es un tipo de iteracion donde siempre interviene un contador. 
		 * 
		 * EL CICLO DE UNA ESTRUCTURA FOR CONSTA DE LOS SIGUIENTES PASOS:
		 * 
		 * - Se inicializa un indice o contador con un valor inicial. 
		 * - Se evalua una condicion. 
		 * - Si lacondicion ha sido falsa termina la iteracion y si la condicion ha sido 
		 * 		verdadera se ejecutan las acciones de bucle.
		 * - Despues de ejecutar las acciones del bucle se incrementan el contador en un valor positivo o negativo. 
		 * 
		 */
		System.out.println("Vamos a ponerlo en practica contando del 1 al 10 en java");
		
		int i;
		for (i=1; i<=10; i++) {
			System.out.println(i);
		}
		System.out.println(i);

	}

}
