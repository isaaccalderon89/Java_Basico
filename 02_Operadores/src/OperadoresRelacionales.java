
public class OperadoresRelacionales {

	public static void main(String[] args) {
		/*
		 * Los operadores relacionales comparan dos operandos y ofrecen como resultado un valor booleano
		 * (verdadero o falso). Se aplican a cualquier tipo de datos. 
		 * 
		 * < (menor que)
		 * > (mayor que)
		 * <= (menor o igual a)
		 * >= (mayor o igual a)
		 * != (distinto de)
		 * == (igual a)
		 * 
		 */
		
		boolean resultado = 3>5;//Creamos la variable resultado, e indicamos que el valor de la varible depende de
		System.out.println(resultado);// la operacion aritmetica que hemos visto.

	}

}
