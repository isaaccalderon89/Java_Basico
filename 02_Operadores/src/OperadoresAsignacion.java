
public class OperadoresAsignacion {

	public static void main(String[] args) {
		/*
		 * Los operadores de asginación se utilizan para asginar un valor a la variable. Este valor puede ser simple
		 * o el resultado de una expresión
		 * 
		 * = (asignacion simple)
		 * +=(asignacion compuesta suma)
		 * -=(asignacion compuesta resta)
		 * *=(asignacion compuesta producto)
		 * %=(asginacion compuesta modulo o resto)
		 * /=(asignacion compuesta modulo o resto)
		 */
		System.out.println("Vamos a ver un ejemplo de Operadores de Asignación");
		
		int a = 5;
		a += 25;
		/*
		 * Estamos realizando una operacion aritmetica. Hemos creado una variable y luego le hemos sumado '25'
		 * en un sencillo paso.
		 */
		
		System.out.println(a);
		
	}

}
