
public class MetodoConcat {

	public static void main(String[] args) {
		/*
		 * METODO CONCAT
		 * 
		 * El metodo concat() devuelve una nueva cadena, que es el resultado de un nueva cadena, que es el resultado 
		 * de concatenar la cadena del objeto al que se aplica con la cadena pasada como argumento. 
		 */
		String texto = "Cocodrilo ";
		String texto2 = texto.concat("Dundee");
		System.out.println(texto2);
	}

}
