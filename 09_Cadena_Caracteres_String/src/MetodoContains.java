
public class MetodoContains {

	public static void main(String[] args) {
		/*
		 * El metodo contains() devuelve true si la cadena del objeto al que se aplica contiene la subcadena 
		 * especificada en el argumento.
		 */
		String texto = "Cocodrilo";
		System.out.println(texto.contains("dri"));
		System.out.println(texto.contains("pot"));

	}

}
