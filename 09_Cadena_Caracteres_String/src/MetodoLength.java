
public class MetodoLength {

	public static void main(String[] args) {
		/*
		 * EL METODO LENGTH()
		 * 
		 * El metodo length() devuelve la longitud de la cadena, es decir, el numero de caracteres que contiene. 
		 */
		String texto = "La cripta magica"; 
		System.out.println(texto);
		System.out.println("Longitud: "+texto.length());

	}

}
